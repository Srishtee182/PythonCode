# Operators available: arthemetic, assignment(used to assign value like =, +=, -= etc, comparision(>=,<=, ==),
#  logical(and, or, not), identity (is, is not), membership(in, not in), bitwise

#arthemetic
print("15 // 6 is", 15//4)# 4 * 3 =12, it will take nearby number less from which 15 is divided
